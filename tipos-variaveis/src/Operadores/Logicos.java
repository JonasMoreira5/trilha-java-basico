package Operadores;

public class Logicos {
    public static void main(String[] args) {
    // Os operadores logicos representam o recurso que nos permite criar
    // expressões lógicas maiores a partir da junção de duas ou mais expressões
    // && operador Lógico "E"
    // || Operador Lógico "OU"

    // Logicos.java 
    boolean condicao1 = true;
    boolean condicao2 = true;

    if(condicao1 && condicao2){
        System.out.print("As duas condições são verdadeiras\n ");
    }
    if(condicao1 || condicao2){
        System.out.print("Uma das condições são verdadeiras ");
        }
    }
}
